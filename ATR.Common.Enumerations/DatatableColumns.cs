namespace ATR.Common.Enumerations
{
    /// <summary>
    /// Defines column types for datatable.
    /// </summary>
    public enum DatatableColumns
    {
        /// <summary>
        /// Only checkbox.
        /// </summary>
        Checkbox,

        /// <summary>
        /// Only detail.
        /// </summary>
        Details,

        /// <summary>
        /// Only delete.
        /// </summary>
        Delete,

        /// <summary>
        /// Only edit.
        /// </summary>
        Edit,

        /// <summary>
        /// edit and delete.
        /// </summary>
        EditDelete,

        /// <summary>
        /// no specific action (view only).
        /// </summary>
        None,

        /// <summary>
        /// Only select.
        /// </summary>
        Select
    }
}
